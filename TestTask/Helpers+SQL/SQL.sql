CREATE TABLE Posts
(IdPost UNIQUEIDENTIFIER PRIMARY KEY DEFAULT newsequentialid(),
NamePost VARCHAR(50));

CREATE TABLE TypesOfDep
(IdType UNIQUEIDENTIFIER PRIMARY KEY DEFAULT newsequentialid(),
NameType VARCHAR(50));

CREATE TABLE Departaments
(IdDep UNIQUEIDENTIFIER PRIMARY KEY DEFAULT newsequentialid(),
FullNameDep VARCHAR (100),
ShortNameDep VARCHAR (15),
TypeId UNIQUEIDENTIFIER FOREIGN KEY REFERENCES TypesOfDep(IdType) ON DELETE CASCADE);


CREATE TABLE Users
(IdUser UNIQUEIDENTIFIER PRIMARY KEY DEFAULT newsequentialid(),
NameUser VARCHAR(50),
SurnameUser VARCHAR(50),
LastnameUser VARCHAR(50),
PostId UNIQUEIDENTIFIER FOREIGN KEY REFERENCES Posts(IdPost) ON DELETE CASCADE,
DepId UNIQUEIDENTIFIER FOREIGN KEY REFERENCES Departaments(IdDep) ON DELETE CASCADE NOT NULL,
TelUser VARCHAR(15),
NationalityUser INT FOREIGN KEY REFERENCES Countries(IdCountry));

